package com.java_db_example.data;

public class PollBooth {
    private int pollBoothId;
    private String pollBoothName;
    private String location;

    public int getPollBoothId() {
        return pollBoothId;
    }

    public void setPollBoothId(int pollBoothId) {
        this.pollBoothId = pollBoothId;
    }

    public String getPollBoothName() {
        return pollBoothName;
    }

    public void setPollBoothName(String pollBoothName) {
        this.pollBoothName = pollBoothName;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }
}
